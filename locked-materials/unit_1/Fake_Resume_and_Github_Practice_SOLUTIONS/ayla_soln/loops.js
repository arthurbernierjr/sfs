// Loops - More Reps!
// There are multiple ways to answer each loop (in addition to the answers below)

// 1. Create a loop that logs the numbers from 0-99 (ascending)
  for(let i = 0; i <= 99; i++){
    console.log(i);
  }

  // or

  let num = 0
  const limit = 99;

  while(num <= limit){
    console.log(num);
    num++
  }


// 2. Create a loop that logs the numbers from 99-0 (descending)
  for(let i = 99; i >= 0; i--){
    console.log(i);
  }

  // or

  let num = 99;
  const limit = 0;

  while(num >= limit){
    console.log(num);
    num--
  }


